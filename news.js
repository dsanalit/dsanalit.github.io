function insertNews() {
  const container = document.getElementById("newsContainer");
  container.innerHTML = "";

  // 1. Sort preferred items first
  const sortedNews = [...newsItems].sort((a, b) => (b.preferred === true) - (a.preferred === true));

  // 2. Create cards
  sortedNews.forEach(item => {
    const card = document.createElement("div");
    card.className = "news-card";
    if (item.preferred) card.classList.add("preferred");

    card.innerHTML = `
      <img src="${item.image}" alt="${item.title}">
      <div class="news-content">
        <time datetime="${item.date}">  ${new Date(item.date).toLocaleDateString('en-GB', {
    day: 'numeric',
    month: 'long',
    year: 'numeric'
  })} </time>
        <h3>${item.title}</h3>
        <p>${item.description}</p>
        <a href="${item.link}" target="_blank">Read more →</a>
      </div>
    `;

    container.appendChild(card);
  });
}

window.addEventListener("load", insertNews);
